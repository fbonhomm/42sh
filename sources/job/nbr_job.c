#include "job.h"

int				nbr_jobs(t_shell *s)
{
	int			i;
	t_job		*tmp;

	i = 0;
	tmp = s->first_job;
	while (tmp)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
